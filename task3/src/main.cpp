#include <iostream>

int main() {
    const int SIZE = 100; // Максимальный размер массива
    double data_array[SIZE]; // Объявление массива
    int count = 0; // Счетчик для хранения количества максимальных значений
    double max_value = -1e9; // Инициализация максимального значения

    int n; // Фактическое количество введенных чисел
    std::cout << "Введите количество чисел (максимум " << SIZE << "): ";
    std::cin >> n;

    // Проверка на допустимый размер массива
    if (n < 1 || n > SIZE) {
        std::cout << "Некорректное количество чисел!" << std::endl;
        return 1;
    }

    std::cout << "Введите " << n << " чисел: ";
    for (int i = 0; i < n; ++i) {
        std::cin >> data_array[i];
    }

    // Поиск максимального значения и подсчет его количества
    for (int i = 0; i < n; ++i) {
        if (data_array[i] > max_value) {
            max_value = data_array[i]; // Обновляем максимальное значение
            count = 1; // Сбрасываем счетчик, так как нашли новое максимальное
        } else if (data_array[i] == max_value) {
            count++; // Увеличиваем счетчик, если число равно текущему максимальному
        }
    }

    std::cout << "Максимальное значение: " << max_value << std::endl;
    std::cout << "Количество чисел, равных максимальному: " << count << std::endl;

    return 0;
}

